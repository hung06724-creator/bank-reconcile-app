const toNoAccentLower = (value: string): string =>
  value
    .normalize('NFD')
    .replace(/\p{M}+/gu, '')
    .replace(/đ/g, 'd')
    .replace(/Đ/g, 'D')
    .toLowerCase();

const toKeyword = (description: string): string =>
  toNoAccentLower(description)
    .replace(/\s*\/\s*/g, '|')
    .replace(/\s+/g, ' ')
    .trim();

