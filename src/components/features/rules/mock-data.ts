// Helper to resolve category_id and category_name from code
function resolveCat(code: string) {
  const idx = ACCOUNTING_CATEGORY_CATALOG.findIndex((c) => c.category_code === code);
  if (idx >= 0) {
    return {
      category_id: `cat-${String(idx + 1).padStart(3, '0')}`,
      category_name: ACCOUNTING_CATEGORY_CATALOG[idx].category_name,
    };
  }
  // Fallback to OTHER
  return {
    category_id: `cat-${String(ACCOUNTING_CATEGORY_CATALOG.length + 1).padStart(3, '0')}`,
    category_name: 'Khác / Chưa xác định',
  };
}

